void oled_display_mode_step(void) {
    current_display_mode = (current_display_mode + 1) % 3;
}

bool oled_task_kb(void) {
    if (!oled_task_user()) {
        return false;
    }

    static bool logo_rendered = false;
    if (!logo_rendered) {
        render_logo();
        if (timer_read() > 2500)
            logo_rendered = true;
    }
    else {
        switch(current_display_mode) {
            case 0:
                render_layers();
                break;
            case 1:
                render_button_pressed();
                break;
        }
    }
    return false;
}

